# Basic math
a = 5
b = a + 10
c = b - 2
d = c * 3
e = d / 6
print(e)
e = (a + 10 - 2) * 3 / 6
print(e)

# Logic operations
print(True and False)
print(True or False)
a = True
print(a is True)
print(a is False)
print(a == 5)  # a double equal sign asks if the things on either side are equal
print(a != 'cake')  # the != sign asks if the things on either side are not equal


