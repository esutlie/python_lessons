"""
Types of data
There are four basic types of data that we use in python:
Integers, Floats, Strings, and Booleans.
"""

# Integers
print(1)
print(2)
print(-3)

# Floats (aka numbers with decimals, no fractions allowed)
print(1.5)
print(.34)
print(-4.2)

# Strings (letters, words, numbers, symbols, whatever you like enclosed in quotation marks)
print('Hello')
print("My Name is Chris")
print('5 cheese please.')

# Booleans (True or False, can be combined with 'and' and 'or')
print(True)
print(False)
print(True and False)  # Will just evaluate as False
